# Escape chars

# Breaking a string to a new line '\n' is a special char to print a new line
print("This is a long \n ass string, which has a new line")

# Adding a tab space between '\t' is used
print("This will have a tab space \t between now")

# Escaping characters such as quotes ''
print('\'This is awsome and highlighted\' was escaped')
print("\"This is awsome and highlighted\" was escaped")

# Escaping characters such as quotes '' with surrounded "" will work without escaping
print("'This is awsome and highlighted' without escaping")

# Escaping backward slash
print('\'This is awsome and highlighted\' without escaping\\') # <- only a sinlge backward slashed is shown